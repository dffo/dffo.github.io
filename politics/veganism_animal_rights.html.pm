◊initial-cap{The moral worth} of animals is one of man's oldest ethical
quandaries. 
The debate has hardly mellowed with age, as
ancient debates over methods of husbandry and slaughter
have been augmented in recent years by more bloodless questions about
aggregate harms and animal rights. 
But this perennial puzzle makes a useful case study for the
tension at the heart of human society: the need to make compromises
under scarcity, uncertainty, and moral fog. In particular, more contemporary
approaches around animal rights and veganism cleanly illustrate the
ethical tradeoffs that result in the moral economy and political institutions
of a plural society.

We begin by exploring veganism through the lens of ◊i{moral economy}---the
ability to make moral tradeoffs under uncertainty and given competing wants.
We start with intuitive, popular forms of veganism, before shifting to
more sophisticated utilitarian and quantitative approaches. Afterwards, we explore
animal rights as a legal movement and philosophy, weighing its merits and costs
in the context of a ◊i{political economy} where voter interests shape
political incentives. While both movements are admirably earnest,
their neglect of 
the underlying incentives in society risks unintended consequences and backlash.

◊h2{Veganism and moral economy}

Start with veganism and other duty-based approaches to animal ethics. 
Vegans give a variety of arguments for shunning meat and other direct forms 
of animal use. Some are grounded in the shared dignity of living creatures,
with an emphasis on the cruelty of slaughter. Others take a more utilitarian
and empirical approach, 
arguing that avoiding animal husbandry is a reliable way to minimize the 
unnecessary death of living beings. Given the burden of such a commitment,
it is hard to doubt their sincerity.
But trying to quantify the exact effects of such an approach reveals the
underlying tension.

All human consumption sits at the intersection of cost
and benefit. The utility
gained from consumption, whether health or enjoyment, makes the initial costs
worthwhile; but we must account for these costs for to prevent overconsumption.
A market economy allows us to weigh the benefits of consumption against
the scarcity of labour and physical resources by
comparing each cost in a shared currency. 
This not only lets us tune
our own consumption relative to our other needs---for example, buying
fewer bags of coffee to save money for rent. It also lets us make tradeoffs
between substitutes in the same category---for example, buying instant
coffee to save money over whole beans.

These
twin concepts of ◊em{marginal tradeoffs} and ◊em{fungibility} are what make the
system work.
Prices force us to constantly weigh
the value we gain from buying things
against the resulting toll on the world's resources,
or to find new ways to use what we have more efficiently.
They discipline our habits of consumption through the lenses of physical scarcity
and labour.
Veganism effectively adds to these a new class of ◊i{moral costs}.
But to minimize overall harm under this new lens, we then
need to be able to weigh moral costs both against each other and against
our competing needs---that is, we need a ◊i{moral economy}.
This is where veganism's utilitarian claims come under strain.

Let us first look at popular forms of veganism. Popular veganism
assigns all direct animal use---whether meat, milk,
or animal testing---an unbounded moral cost
(more precisely, a ◊i{lexically prior} one), while leaving indirect
animal harm effectively unpriced, be it through infrastructure,
fuel, or industry. But such a
moral economy stumbles on both counts.
First, it doesn't allow us to make marginal
tradeoffs, because an unbounded cost is inherently unbeatable.
Imagine the mayor wants to pave a rural road. He estimates that the
project would kill a dozen deer. 
Should the project continue given those harms?
What about a half-dozen deer? Without tradeoffs the question
is unanswerable.

Second, the lack of fungibility causes problems. What if instead the road killed 
a dozen squirrels instead of deer? Or a dozen ants, or spiders? Would that
make the project more defensible? 
Of course, veganism doesn't really focus on such cases, as they don't
involve the direct consumption of animals. 
Yet the fact that veganism
leaves such large swathes of human activity effectively unpriced means that it is
impossible to guarantee that a society following its tenets would actually
reduce animal harm. There are too many free variables.

It may seem unfair to press veganism with such examples, since the
framework focuses on other domains of human activity such as agriculture.
But even here, the lack of fungibility poses problems
for popular veganism: is killing a single
cow worse than poisoning a thousand beetles? 
Let us thus shift from popular veganism and
steelman a pure utilitarian vegan that treats all animals equally.
The strongest
argument against raising livestock is not
mammalian worth, but trophic efficiencies: raising cattle requires
growing far more plants to use as feed than if we simply ate the plants
directly. In that sense, it is very plausible that a herbivore society would
need to kill fewer animals to eat.

Unless we explicitly prioritize mammalian worth, the order-of-magnitude increase
in harm to produce the feed likely dominates the total harm of livestock.
But note the implication. If husbandry's greatest sins are not of kind but
degree, such harms scale with ◊em{all} forms of consumption. 
Meat causes more harm because it consumes more resources. 
To isolate veganism's effects from a general turn towards asceticism,
we must therefore hold society's aggregate demand fixed.
If people ate more plants,
they would then use the money saved to shift their consumption to
travel, sport, or real estate, which also harm ecosystems and animals---a 
◊i{substitution}, not a reduction of harm.

Meanwhile, more sophisticated approaches to veganism that try to weight animals
risk becoming intractable.
Such a task would require valuing every animal life in some common unit of account,
and then determining the ◊i{social cost} of that basic unit in dollars.
This would not only be highly subjective;
it would be mathematically chaotic, with the results being highly sensitive
to the underlying assumptions.
Agreeing on any such framework would be impossible without some amount of 
hindsight fudging. 
Assessing a ◊i{social cost of carbon} in climate economics is already fraught,
but this would be far worse, 
sprawling out 
into an entire command economy of moral pricing. 

We can see that veganism stumbles as a heuristic due to its arbitrary
pricing of harm, while any finer-tuned system becomes untenable.
Neither solution appears to be a better heuristic of animal harm than 
a general measure of consumption. 
While imperfect, aggregate consumption is non-distorting, broad-based,
and easy to measure.
While a consumption tax has its own share of complexity, it
would naturally push people towards activities that consume fewer resources,
making it perhaps a more practical heuristic for reducing overall harm.
Veganism, meanwhile, is more focused on
an intuitive moral hygiene than mathematical rigour.

◊h2{Animal rights and political economy}

Let us now examine another contemporary movement in animal ethics: the animal
rights movement. We have seen a push to bring suit against men and corporations
not only by their fellow men, but increasingly
on behalf of animals and nature itself. This is a novel approach to animal
welfare. Yet while it can
feel morally satisfying to have nature itself win a case against a scoundrel,
it brings institutional risks. Such a critique does not deny the moral worth
of animals. That has long been recognized in human custom. 
The question is whether
legal rights for individual animals are a workable approach. 

Human societies have long
had a rich set of customary rights and norms governing animal use, but these are
enforced ◊i{ad hoc} and through negotiation rather than judges, lawyers, and the
Leviathan of the state.
Legal rights in this context are pragmatic and instrumental—their job is to allow men
to live together without coming to blows by regulating the use of force. As Burke
wrote in a flash of realist insight, 
◊q{government is a contrivance of human wisdom to provide for human wants.} 
It is created by men not to bring heaven to Earth, but to allow them to live
together while negotiating their differences.


Whatever the moral or metaphysical justifications people give for individual 
human rights, the reality is much simpler. In self-government, society is most 
stable when each person within it feels safe. The most robust way of achieving 
this is through reciprocity: I agree to respect other people's dignity so that 
my own dignity will be respected. If I start to trample on other people's 
dignity, I am essentially making a bet that I am stronger than the collective 
ire of every other person in society—a risky gamble. 

The benefit of such a system is that it is predictable, since punishment emerges
entirely from one's own actions; and it is based on action rather than factional
loyalty. This makes it an easy sell to all members of a society, no matter how
physically weak or poor, and lets them pool their power behind a rule-bound
state that towers over even the greatest rogues and scoundrels.

The system works because it is anchored in the ◊i{interchangeability} of all
humans in society. This is what allows it to protect even the weakest among us,
and its limits show clearly in our institutional fabric. The disabled, who
cannot fight for themselves, are protected because each of us has the capacity
to become disabled. I respect the disabled so that I should be so respected if
something happens to me. I respect the sick, and the dying, and the unconscious,
for the same reason. Importantly, this is equally true of a man who is sick
with an illness I can never have; interchangeability does not require perfect
equality, but an equality of ◊em{vulnerability}.

The case of children is illustrative: having become an adult, I cannot become a child
once more---but I can become a parent.
Accordingly, societies are often sensitive to
parental discretion in how they raise their kids. 
Such parental rights emerge as a reciprocal
accommodation: ◊i{I will let you raise your kids in good faith
if you let me do the same}.
This approach reflects both the unique burden borne by every parent
and the need to avoid top-down
impositions that turn differences of opinion or culture into conflict.
But even so, we still guarantee children a baseline of rights as human beings.
For weakening the dignity of human life, however young or old, has a chilling
effect on all of us.

We can see that individual human rights are not a moral nicety imported into
our system of government. They ◊em{are} the system. Whenever we add extra
conditions to our legal foundations, such as giving a legal personhood to
animals or nature, we burden these institutions with extra constraints not
necessary to their stability. Indeed, if treating animals this way puts
strain on human welfare (for example, by making food more expensive or
threatening human culture), it opens up a space for populists to claim that
the voters are being ill-served by their laws and government.

The argument is not simply that change is divisive, and so should always be avoided.
Indeed, many of the greatest social reforms were a vindication of human dignity
through reciprocity and interchangeability. Take the abolition of slavery, for 
example. Abolitionists did not think that the institution put them
directly at risk of enslavement. But many argued that a society in 
which a segment of human beings could be dominated by an oligarchic ◊i{Slave Power} made
despotism more likely for all. Marx saw slavery 
as portending a future domination of ◊em{all} workers by wealthy capitalists.
The critics saw within slavery a reflection of their own human frailty;
race was merely a fig leaf for a deeper ill.


Even aside from this, a framework of personhood is ill-fitting for animals, because
there are too many cases where we need to treat animals collectively rather than
individually. Think of feral animals that endanger birds, or invasive species that
threaten ecosystems. We may need to cull animals to preserve the whole, which is 
incompatible with an individual view of animal rights. This need to sometimes
view animals as collectively valuable clashes with personhood in a way that
no other expansion of the category ever did. It raises serious questions about whether
such an expansion is the right approach.

This doesn't mean that laws protecting animals cannot exist, although calling
them ◊i{animal rights} distorts their intrinsically human nature. The most
defensible laws are framed as constraints on human behaviour that seek to
minimize gratuitous violence while avoiding burdensome costs, rather than
creating a legal fiction of animals as plaintiffs. Such fictions too often
lapse into moral ventriloquism, giving power not to animals but to those who
affect to speak for them. The resulting fog risks breeding cynicism—the idea
that the legal system is a web of fictions meant to privilege the strong over the weak.

The recent trend of populists trying to ban lab-grown meat clearly illustrates the case.
Such bans don't immediately make sense on their face, as giving customers the
option to buy such products doesn't take away their right to buy meat from livestock.
While some may see it as just a sop to farmers, many politicians 
◊a[#:href "https://www.bbc.com/news/world-us-canada-68947766"]{explicitly frame it} 
as a reaction to animal-rights absolutism: ◊i{if lab-grown meat takes off,
the elites are going to foist it on working families. Better to nip it in the bud 
beforehand.} 
Food is bound up with identity---top-down attempts to optimize it away
risk evoking a glib technocracy, with people being reduced to entries
in a spreadsheet by planners with no special moral insight.


Tragically, the problem with animal rights advocates is not the moral substance
of their claims. It is rather the political economy of their prescriptions.
I believe that they are animated by an admirable zeal, but their desire
to assert the unity between man and his fellow creatures should be rhetorical
rather than legal. Government is not meant to give flesh to metaphysical truth,
but to hold a plural society together. We all love animals. That alone should
give their advocates hope.

◊h2{Conclusion}

These case studies show the risks of using duty-based or metaphysical
frameworks as an approach to public ethics.
Human societies have developed a diverse set of moral rules and customs
that help us cope with the tragic nature of life and death that animates
this world. Veganism and animal rights follow in this approach; they do
not sit apart from them, nor are they intrinsically better than their
venerable peers. The best approach then is to avoid absolutist, moralizing claims about
animal rights. It is one of man's oldest debates; that it has festered
unresolved for so long suggests that there is no obvious answer to it.

Grounded in pluralism and mutual respect, we can then try to find
points of alignment between human dignity and animal dignity.
We have long forbidden sadism, torture, and abuse of animals for this reason.
Further improvements should be in this mould. As our knowledge grows, it should
raise our ambitions, allowing us to reduce suffering without forgetting human needs.

Most of these values can and 
should remain customary and negotiated. 
We must resist the illiberal impulse to impose one answer upon
communities doing so in earnest, whether that be bans of lab-grown
meat or kosher slaughter.
But if government is made in the service of human wants,
we should also not forget that most humans want animals
to live happy lives. 
The constraint is not our indifference to suffering, but the world's. 
And as we navigate this vast cycle of life and death, we must strain to avoid
sacrificing our own needs to a fruitless idealism—or giving in to an ashen
cynicism.
