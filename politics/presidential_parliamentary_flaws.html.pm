◊(title "Beyond the Parliamentary--Presidential debate" 
"Legislative diffusion and the executive ratchet" #:big? #f)
◊(print-date)

◊initial-cap{Few debates are} more confused by terminology
than that between the ◊i{parliamentary} and ◊i{presidential} systems
of government. People have argued for decades over which
leads to better governance, which is better at avoiding
populism, and which resists democratic backsliding more effectively.
But such debates, even when they have the self-awareness to
add a third, ◊i{semi-presidential} category, often group together
countries whose constitutions are more different than alike.
Not only does this make such comparisons less useful, but it also
hides the deeper causes of polarization and authoritarianism, which
can plague any democracy regardless of its outward form.

◊;{Thesis paragraph}

The biggest mistake of such debates is to focus on the
separation between legislative, executive, and judicial powers
when comparing the two systems. Instead of focusing on
◊i{interbranch} conflict between the executive and legislative powers
as the source of gridlock, it is far more important and illuminating to look at
the ◊i{intrabranch} distribution of power---namely, the diffusion
of legislative power among many different actors, whether courts,
senates, or executive vetoes.
And when searching for the causes of authoritarianism, it is far
more illuminating to look at the increasing concentration of power
in the executive branch in both presidential and parliamentary regimes.

◊h2{Legislative diffusion}

Given all the ink spilled over the merits of the separation of powers,
it can be disorienting to remember that no democratic system on Earth
has ever cleanly separated the judicial, legislative, and executive roles.
The United States' Constitution gives the President a qualified veto
over Congress, and its Supreme Court has the power to nullify its Acts
beyond mere statutory interpretation. This may seem natural to Americans
today, but it was highly contested during its ratification, with the
antifederalists criticizing both in scathing terms. In particular, they
worried that the presidential veto would lead to the executive branch
dominating the legislature.

In ◊i{The Federalist}, Alexander Hamilton offered two responses to these
criticisms. First, he argued that the veto would be seldom used,
just as the royal veto in Britain saw sparing use. Second, he argued
that even if it were used, it would be better to err on the side of fewer
laws passed regardless, as more lawmaking could lead to more mischief.
While many of the early presidents accordingly reserved the veto for laws they
thought to be unconstitutional, by the end of the nineteenth century it had
become a routine and partisan device.

The presidential veto might seem a trifling thing. But its effect on
the legislative power is immense. The most striking way to illustrate
this is to try and mathematically model the effect of a veto within
a legislature. If we focus solely on the lower house for simplicity,
a veto with a two-thirds override effectively creates two ◊i{virtual}
blocs in the House, whose total votes add up to a third of
the chamber. The first of these
blocs perpetually votes ◊i{no} on all acts, while the other votes
with the president's wishes. A president who supports
an act has no effect on the legislature, as the two virtual
blocs cancel out. But if he opposes it,
two-thirds of the real members are needed to reach a majority.

This doesn't have a great impact if the presidency and legislature are
controlled by the same party. But when the legislature is elected
separately from the president, as with America's midterm elections,
this virtual legislative faction takes on a life of its own.
In effect, it turns the legislature into a strange kind of multiparty
system, with shifting factions that must constantly negotiate to
pass new laws, even if there are nominally only two parties.
In reality, the legislative power is now diffused among several competing
power centres---one of which doesn't even sit in the legislature.

Likewise, actual multiparty systems also naturally create legislative
diffusion. This is an explicit feature of parliaments that are elected
with ◊i{proportional representation}, but such results can also emerge
in ◊i{first-past-the-post} countries with regional coalitions.
The key factor is how much ◊i{monopoly power} a given political party or
coalition has over the factions it comprises. A two-party ◊acro{FPTP}
system tends to give its two parties a great deal of monopoly power,
forcing its factions to coalesce before election time. 
Legislative power is then concentrated in the party leadership,
who have a free hand to test out their ideas until the term ends.
Under ◊acro{PR}, every new bill potentially requires negotiation.

A strong upper house also adds a possible site of legislative diffusion.
If it has the power to block laws, it creates yet another power centre
that must be brought on side to use the legislative power. When combined
with all the other potential sources of diffusion, 
the legislative power often becomes the hardest to actually use when
compared with the
executive and judicial powers, despite being more representative
of the people than the others.

While Hamilton was skeptical of a
hyperactive legislature, such constraints simply freeze the status quo rather
than limiting government. Indeed, having to navigate legislative
diffusion can make it harder for reformist governments to tackle special
interests, making government less accountable.
In a vibrant democracy, each party has a thesis
for how best to make their country better off; voters choose the party they
find most credible, and change their votes over time as their priorities shift.
When neither side can put their ideas to the test due to legislative diffusion,
both sides strain fruitlessly against one another, with causal beliefs
crystallizing into near-sectarian identities.

Importantly, such gridlock has little to do with the parliamentary versus
presidential debate. What matters is legislative diffusion. Presidential
countries like Ghana are strikingly gridlock-free, lacking an upper house,
having a robust, two-party system, and no midterm elections that cause
divided government. Meanwhile, Argentina is the complete opposite scenario:
highly proportional representation, a strong upper house, and staggered
midterm elections that lead to a notoriously gridlocked government. But gridlock
is not exclusive to presidential systems, with countries like Belgium and
Israel having famously drawn-out negotiations due to their electoral systems.

Ultimately what matters in a democracy is how nimbly it can respond
to its citizens' concerns by testing out new ideas. Diffusing the legislative
power makes it harder for a government to be responsive to its citizens.
While coalition-heavy consensus governments can be useful in countries grappling
with sectarian or regionalist conflict,
too much gridlock can lead to disillusionment as it becomes harder
to learn what policies work and to hold parties accountable.

The result is greater pressure on the other branches to pick up the slack.
Courts are a clear example:
when laws are artificially frozen by gridlock, they can come under
pressure to interpret laws more broadly to keep up with new developments.
Any ill-considered precedents become harder for the legislature to
check through statutory revision.
Diffusion therefore massively empowers the judiciary,
even in systems without judicial supremacy.
The temptation for judicial heroism risks politicization and popular disillusionment;
constitutional review only increases the risks.
Yet perhaps the most acute danger of diffusion comes not from the judiciary
but the executive branch.

◊h2{The executive ratchet}

If gridlock's causes are independent of constitutional regime,
the greatest risk of authoritarianism---that of an overmighty executive ruling by
fiat---ironically seems to afflict both democratic systems equally, by completely
different means.
A powerful executive branch is really just the positive form of legislative
diffusion. Rather than giving many different actors veto power over new acts,
it instead gives the executive branch the power to create new laws by diktat.
Even the wiliest political operator would normally have his whims tempered by the slow
pace of parliamentary procedure. But an unmoored executive can legislate
the world at the stroke of a pen.

Allowing the rules to change so erratically erodes the rule of law,
which thrives on consistency and predictability. Ironically, such
executive dominance effectively recreates Hamilton's fears of excessive lawmaking,
by moving it into a form even more capricious and less restrained by compromise.
This is particularly salient in the United States today,
with presidents being criticized for using executive agencies to hound political
rivals and disfavoured organizations.
It results from a centuries-long process by which
Congress has delegated its powers to the executive branch,
with additional checks like the legislative veto fading away or being
struck down by courts.

But this ill is equally pernicious
in parliamentary systems. Perhaps unintuitively, the parliamentary
model does also have a real separation of powers. Cabinet ministers cannot
simply create laws by fiat---they must introduce a bill into parliament,
with all the procedural speed bumps that entails.
But once parliament passes a law granting executive powers to the
cabinet or external agencies, those powers can now be used with 
greater speed and less restraint.
In parliamentary countries like India and Hungary, the government is often
criticized for using its executive powers to pressure journalists,
throttle the internet,
and selectively investigate opposition politicians.

Indeed, the greatest risk is not from the overzealous use of
laws delegated to the executive, but from their lying dormant,
ready to be weaponized against disfavoured citizens.
It calls to mind the famous Soviet line: ◊i{Give me the man,
and I'll find you the crime.}
Sprawling tax codes, regulatory regimes, and copyright laws are all
potential weapons of abuse.
When every citizen has broken some law, often unawares, the law itself
becomes a veil for arbitrary power.
Once the executive is seen as a mere vehicle for vindictive lawfare,
capturing the office becomes a kind of Hobbesian anarchy, with each
side trying to preemptively seize that power before their opponents
can use it against them.

Executive dominance is clearly a risk in both kinds of democratic systems.
But what is remarkable is that both systems conspire towards a creeping
growth of this executive power by completely different mechanisms.
This growth, which I call the ◊i{executive ratchet}, seems to me a
fundamental flaw of every democratic system in use today. 
As it continues, it risks corroding institutions, with elections becoming
an existential race to control an unbounded, capricious use of state power.
The result is an escalatory death spiral.
The best remedy is to reverse the ratchet, yet systemic flaws make
that unlikely in both regimes.

In the presidential system, the mechanism for the executive ratchet is
structural: the presidential veto freezes the status quo in a manner
that naturally favours the President. It is thus much easier for Congress
to delegate its powers to the executive than to claw them back. 
When Congress is controlled by the President's party, the last thing they
would want is to humiliate their own ally in this way. And when Congress
is controlled by an opposition party that wants to check presidential excess,
they often don't have the numbers to override the President's veto.
And so the President's powers only grow.

In the parliamentary system the executive ratchet works differently,
as the same leader holds both legislative and executive roles. In this case
the ratchet works less through structural forces and more through game theory.
Even if an opposition candidate rails against executive abuse by the government,
once he gains power, how likely is he to cast it into the fire having just
gained the chance to do so much at speed? Voters usually care about results
rather than process: they vote for parties because they want their own lives
to improve, be that the economy, security, or social order. 
A new Prime Minister will want to please his voters quickly.
Why surrender the best tool to achieve it?

It is the outcome-oriented nature of the electorate that clicks the ratchet
into place.
An overzealous executive may well alienate the electorate with overly
intrusive enforcement. People are very sensitive to this kind of regime;
they bristle at the thought of living in a ◊i{police state} or ◊i{dystopia}.
But because the public naturally focuses on the visible symptoms 
rather than the underlying cause, public backlash is far likelier
to make the government pull back on enforcement, rather than attacking
the root cause by seeking to reverse the delegation of powers.
And so governments let their powers go dormant. Yet those powers still
exist, latent, to be used on a whim by any future executive. 

That is not to say that the ratchet is completely inexorable. If backlash
is ferocious enough, a future government may genuinely push to restructure
the balance of powers. But this kind of reform is so fraught because it
requires the leadership to unilaterally disarm. Even if the ratchet
allows for reversal, it structurally favours accrual. That 
bias can turn democracy into an existential, zero-sum battle to gain
almost unchecked power over people's lives. That is the most worrying failure
mode of democracy, and it seems to exist in every present form of self-rule.

◊h2{A better approach}

Given how both systems seem to conspire towards this perverse outcome,
it may seem that the executive ratchet is an inevitable outcome of
democracy.
But that needn't be the case, and the presidential model itself already
has the basic ingredients needed to avoid it. 
If the President overreaches in using his executive powers, the resulting
backlash often leads to an opposition party controlling Congress, but
not the executive branch itself. While the opposition cannot directly
take control of the executive power, they do have the option
of clawing back those powers using the legislative branch, or pursuing
more drastic options like impeachment.

This is the key: the solution to executive overreach should be pulling
back those overly broad powers, rather than simply replacing the
executive with someone more popular.
Giving an opposition control of the legislature,
but not the executive, funnels the backlash into the healthiest means of
checking abuse.
The problem with the presidential system is not the separation of powers,
but that the separation of powers doesn't go far enough.
Namely,
the veto allows a President to frustrate the legislature's efforts to rein him
in, causing the ratchet to continue.

Perversely, initiating an impeachment requires a smaller threshold
than the veto  override, with the former requiring a simple majority
of the house rather than a two-thirds supermajority.
This makes it easier for the legislature to begin dismantling
the executive rather than reclaiming the powers that enabled abuse in the
first place. Given that impeachment is such an emotionally weighty device,
such a design also risks fueling polarization and partisanship.
It is better to channel such energy into correcting the powers given to
an erring executive than trying to oust him.

Any such changes are unavoidably speculative, as they would require
major amendments to the constitution. But I believe the best constitutional
design would be to limit ◊em{intrabranch} diffusion while making the
◊em{interbranch}
separation of powers much more robust.
Strengthening the separation of powers would entail weakening the President's
ability to interfere in the legislature by removing his veto power.
The other branches can then check the executive with a clean symmetry.
An executive that acts untethered from law can be checked by statutory 
review, while an executive that pushes his delegated powers too far can have
those powers adjusted by the legislature. 

Reducing intrabranch diffusion would be the more quietly radical part.
Putting each branch under control of a single coalition allows that party
to test out their ideas for improving the general welfare, while forcing
them to take responsibility for the results.
Majoritarian rule can become a concern in countries divided by sect or tribe,
so such intrabranch consolidation should happen within those constraints.
Electoral systems like ◊acro{FPTP} can serve
as a surprisingly good practical compromise here. In balkanized nations,
they naturally yield coalition-heavy politics, as in India and Malaysia, while
allowing more harmonious countries to be accountable and decisive, as in Ghana.

Meanwhile, consolidating the intrabranch power of the executive allows for perhaps
the cleanest check on legislative tyranny: prosecutorial discretion.
Presidents already have wide discretion in how they enforce laws, as often
seen in the case of immigration, narcotics, and tax evasion. Whereas a veto
freezes the status quo, often in favour of presidential power,
discretion allows the president to constrain the use of powers given to him if he
finds them excessive. There is certainly a risk here, but it is a risk
that errs on the side of underenforcement rather than despotism. The voters
can still discipline any presidents they find overly passive, to
prevent discretion from sliding into anarchy.

Some may recoil at making the prosecutorial power more overtly political
(though in practice this has long been so).
Would this not lead to arbitrary rule by the executive? But that is the
point. The executive can only be as arbitrary as the statute permits---in which
case the healthiest check is for the legislature to see the abuse
and prune those powers in response. That is
far better than the present approach of allowing executive powers
to grow in a half-enforced haze. If a more focused executive is likelier
to illustrate the potential flaws of statutes, that is no bad thing.

Such a system would settle into an almost game-theoretic equilibrium.
A president would hesitate to overreach because he knows that an opposition
legislature could claw back those powers from him. And the legislature
would be hesitant to delegate broad powers to the President, as those
powers could then be license for abuse. Meanwhile, the judiciary could
rely mainly on statutory review, as the excesses of each political branch
would naturally be checked by the ambitions of the other---the exact goal of Madison
all along.
With the enfeebling veto removed, the legislature would then become more responsible
and less performative, a true coequal branch.

◊h2{Conclusion}

It may seem naïve to try and construct systems that are held together
by politicians' restraint. Why assume that a President would
use his powers with prudence? Why assume that the legislature would
take care in delegating its powers? As such an oft-caricatured
profession, it is too easy to take the cynical view of politicians
as con-men and megalomaniacs. Such a view would imply that we need
to weaken our politicians at every step, by diffusing their powers
and blocking their ability to make changes.

But perhaps the most striking feature of our politics is just how much
of it works purely on restraint. In the ◊acro{US}, states can abolish municipal
governments
at a stroke. Both state and federal officials can defy court rulings.
And most remarkably of all, the President can launch
nuclear weapons by his sole discretion. Considering how terrible a power that
is, it can be darkly
funny to remember all the constraints and obstacles placed around
something as mundane as the budget. But it shows that restraint is the rule
in self-government, not the exception. It is the extent of diffusion
and cultivation of restraint that characterizes each democracy, whatever its
paper description.

To be sure, there are interesting differences between parliamentary
and presidential models that do have some impact---confidence measures versus
impeachment, for example. But ultimately, the most important questions
have an almost primal earthiness. Who decides? Who wields power?
And most importantly, what impels him to use that discretion
and power with restraint? Every country answers these questions differently.
Some create formal checks that seek consensus, while others instead
emphasize clearer accountability.
Neither answer is inherently wrong; but to understand the tradeoffs,
it is essential that we move beyond the labels and begin to
ask the right questions.
