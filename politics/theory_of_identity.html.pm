◊(title "A functional theory of identity" 
"An inquiry into how identities form, enrich our lives, and cause conflict" #:big? #f)
◊(print-date)

◊initial-cap{There has perhaps} never been a force as powerful yet
ephemeral as ◊i{identity}. It spans an enormous range; from the playful,
like sports, film franchises, and fandoms, to the grave, like language,
race, gender, and nationhood. As modern politics has become increasingly
organized around identity, we have increasingly seen how layered people's
identities are, and how deeply it shapes their self-conception.
Yet just as strikingly, there are many people who do not seem to view
identity so personally, even when they are no less passionate about
their interests and background.

Understanding identity means navigating these seeming contradictions.
Why does it form around the distinctions that it does? How do some
identities emerge seemingly overnight, while others build up unconsciously
over generations? How do people have such different views of identity,
from those who declare themselves ◊i{beyond labels} to those who find
such meaning within them? And why do people fight so fiercely over who
belongs to a given identity, even for identities where the stakes
seem low?
These are the questions this inquiry seeks to answer, surveying
identity in all its forms; sparing neither high-brow nor low-brow,
lighthearted nor solemn.

We first explore what identity is, before discussing
how it emerges from self-reference and relation, using ◊i{linguistic
aspect} as a case study.
We then examine the different ways in which people perceive and use identity,
distinguishing between ◊i{experiential} and ◊i{narrative} modes of identity.
From there, we develop a three-part
framework---◊i{interior state}, ◊i{interface}, and ◊i{social capital}---for
understanding how identity shapes experience and behaviour once formed.
We discuss the tensions that emerge from these different functions.
We conclude by examining ◊i{reification} and the
risks in allowing identities to crystallize in ways that can smother the very people
they are meant to serve.

◊h2{The roots of identity}

One of the most remarkable features of identity is that it is an abstraction
of an abstraction. 
Identities are themselves not physical things, but clusters of social relations
that form around patterns of human traits or behaviours, 
be that language, sect, or nationality.
Yet despite how natural it feels to form identities around such groups,
it can be disorienting to realize that
language, faith, or ethnicity do not really exist as the tidy, bounded entities
that we often take for granted. They themselves are abstractions,
and it is only when we peel back their layers that we realize just
how indistinct and arbitrary they can be.

The English language, for example, is a convenient fiction, a kind of
statistical average over millions of people whose native tongue shares
certain relevant features. People whom we group within the category of
◊i{English speakers} may be able to understand one another, but that isn't
guaranteed---consider the English spoken in the Scottish Highlands,
or parts of India or Singapore where it has been spoken for centuries.
Speakers of a non-prestige language might not even know it has a name---think 
of Singlish or Scots.
This is how the basic ingredients of identity first appear:
◊i{this is how we speak}. ◊i{This is whom we pray to}.
They are behaviours, floating free without reference to a whole.

A category like language might seem to be an illusion born of averages.
But it is an illusion that its speakers
make real, as they come to perceive other speakers as having a kinship with
themselves. This perception needn't track any objective feature of language;
indeed, people who speak near-identical tongues might insist otherwise,
as we see with speakers of Serbian and Croatian. And speakers of mutually
unintelligible languages might simply see them as dialects of the same tongue,
as with Chinese.
And yet despite how arbitrary the concept of language may seem, once named
it takes on a new life: growing, evolving, and yielding children
(as Latin begot French, for example).


We can see that the roots of identity lie in ◊i{self-reference} and
◊i{relation}: to identify what one is doing (speaking a distinct language),
and then relate that to how other people behave around you 
(fellow speakers versus foreigners).
But this process where acts crystallize
into identity is not a simple binary of existence or nonexistence.
Its emergence, comprehensiveness, and utility vary greatly both
across communities and people. 

Consider the following three phrases: 
1. ◊i{I am cooking};
2. ◊i{I cook}; and
3. ◊i{I am a cook}.
The first sentence (which uses
the ◊i{progressive aspect}) simply describes an event taking place,
with little concern paid to the nature or identity of its instigator.
The second sentence (using the ◊i{simple present})
begins the journey towards creating a new identity. 
It introduces self-reference by turning what is normally an event
into a disposition: ◊i{it is in my nature to cook}.
But that itself is not sufficient to create identity, because it
doesn't involve narrative self-placement. In relating a disposition, it fulfills
our first criterion of recognizing oneself, but it does not yet place the
self relative to others.

That only begins to happen with ◊i{I am a cook}, where ◊i{I} is placed beside
a noun ◊i{cook}, which itself condenses a set of behaviours, social norms,
and shibboleths. It is almost like saying ◊i{projected along the relevant axis,
I become interchangeable with all other people of this kind}.
That may bring certain benefits, both practical and emotional. 
Others can expect you to have a certain
expertise, lived experience, or solidarity. But it can also bring a new set
of burdens, as your claim to that identity must eventually meet the test of
reality.

Of course, crystallizing an action into a noun doesn't itself create identity.
We constantly take up roles that must be socially legible to others:
◊i{I am a tourist}, ◊i{I am a patient}, ◊i{I am a supervisor}. These
are useful instrumentally, as they let others work with us more easily.
They might even shape one's conception of the self, for a time. But they are
fleeting. Identity can only form when the categories are stable enough
over time for a shorthand to become useful, and for a cultural narrative
to become sticky.

Identity usually attaches to traits that involve or engender some form of ◊em{knowledge}
or ◊em{experience} that can be generalized over a group. This knowledge can be
practical, for example knowing a language or craft. But it can also emerge
from shared experiences
if members of the group are
treated differently by outsiders. 
Traits lacking this tend not to yield a 
deep sense of identity.
For example, eye colour seldom yields a stable identity,
while left-handedness may if such people face a shared stigma.
And many national identities are forged during times of war, when
people are forced to live or die together. 

Once an identity forms, it can then feed back into
knowledge and behaviour. For example, people who pray similarly
can form a church that then standardizes rites and orthodoxy.
People who play musical instruments can standardize curricula and notation.
Ethnic minorities can create and pass down oral histories and traditions.
And speakers of a language can standardize its spelling and vocabulary
(for example, compare Hindi with Urdu).


◊h2{The experiential and narrative modes}


But even once an identity forms, it is often experienced very differently
from person to person. This is because of identity's dual roles as
both a shorthand that allows for social legibility and as a role that
allows people to situate themselves in their own internal narrative.
Those who value identity for the former use tend to see it more pragmatically
and instrumentally, while those who value it for the latter often see it
as an emotional and epistemic bulwark.
I refer to these two approaches as the ◊i{experiential} and
◊i{narrative} modes of identity.
Their differences show why many conflicts over identity occur, even though
most people mix the two approaches across their many identities.

When identity operates in the experiential mode, 
it is often seen as a tool---a useful means of condensing
abilities and interests into a single, widely recognized word.
In that sense, identity is descriptive and pragmatic. Think
of the musician who so describes himself to find others to
jam with, or a cook who seeks out others to share recipes.
Someone born and raised in Italy might call himself ◊i{Italian}
so that new acquaintances know his native language, music, and cuisine,
while a ◊i{video-gamer} might use the term to seek out competitors.
Such people do not lack identity, but keep it downstream of their
inner selves. 
It is simply a means to efficiently seek out new experiences.

This often lowers the stakes around identity.
For example, the gamer in our example would probably not be offended
if someone accused him of ◊i{not being a gamer}, as his ultimate
goal is to find others to play with. If the term is too vague,
he might use a more specific one, such as ◊i{mobile gamer}, but
it would not hurt him. And if a man raised in Italy
and steeped in its culture was accused of
not being Italian, he would likely be more confused than angry.

This is different from when identity operates in a 
narrative mode, where identity becomes a source of personal meaning.
In this mode, people are likelier to be hurt by such accusations, and
to call out those who gatekeep claims to identity.
For example, many Italians see
◊i{Italianness} as an important part of how they perceive
themselves, both in
Italy and in the diaspora. 
They are more likely to craft
shibboleths about what ◊i{true Italians} do or like,
and bristle at accusations of falling short.
Similarly, a gamer in the narrative mode might bristle
if his identity is questioned due to his taste in games.
And there might be a mix: an Italian gamer who uses ◊i{Italian}
narratively but ◊i{gamer} experientially.



◊h2{The three-point framework}

We can now formalize these insights into a three-point
definition of identity, comprising 
◊i{interior state}, ◊i{interface}, and ◊i{social capital}.
Crucially, we will explore how the experiential and narrative modes
tend to place different weights on each of the three
components, and how these differences often lead to conflicts
over who gets to claim or profit from a given identity.

As noted previously, the ultimate base of identity is a pattern of
shared knowledge or experience over a group of people. Once
this pattern is given a name, and the people therein become aware
of their membership, they create an identity that can grow
and change over time. Each of the three components of identity
extends from this base of shared knowledge: it shapes how
we perceive the world and ourselves, how we communicate
with peers and outsiders, and how much prestige and clout we receive
from society as a whole.

◊h3{Interior state: how knowledge shapes perception}

The ◊i{interior state} refers to how we perceive ourselves
and the world around us through the lens of identity.
Earlier, we
discussed how identity requires ◊i{self-reference}---for
a person to recognize within themselves certain habits, dispositions,
and wants. 
The interior state generalizes this to describe both
our view of the self and the world
as filtered through the identities we recognize.
These two components---the ◊i{self} and the surrounding world---are
distinct, and treated differently depending on the mode of identity.

The experiential mode tends to be outward-facing, focusing on
the surrounding world as a field of potential experiences.
Despite viewing identity more pragmatically, the experiential mode
can still have a rich, filtered perception of the world. It sees life as a series 
of potential experiences, unlocked by interacting with the world 
through one's skills and the ability to coordinate with others.
For instance, a musician in the experiential mode can appreciate the 
music he hears on a deeper level, and imagine
the possibilities of playing with others. 
His skills, perceptiveness, and affordances define his
interior state.

Though he uses identity pragmatically,
an experientially-driven person may still feel that
his interior state has been hurt if his identity is questioned. 
Not because it denies his sense of self, but because it closes off a region 
of potential experiences. Think of the amateur musician who 
is turned away from the club for a lack of skill. 
He might not feel bad because his identity as a musician is 
questioned, but rather because the range of possible experiences 
has shrunk.

The narrative mode works differently.
It instead turns inward, towards the self as
situated within a story and community, and then uses that sense of 
the self
to interpret the surrounding world.
It often asks questions about self-belonging,
and tries to understand the world relative to that group membership: 
◊i{how does this shape who I am?} 
◊i{Who are my kin within the community?}
◊i{How do I feel about this event given my identity?}
In the narrative mode, people often feel even greater pain when
their identity is questioned. 
It can destabilize their own personal stories,
shaking their understanding of themselves and their role in the world.

The narrative mode places great emphasis on how one understands
oneself. Indeed, people with a strong narrative identity
often endeavour to show others how they feel inside through
various means of self-expression. They may tattoo symbols representing
their interests onto their bodies, or display them
prominently on social media or clothing.
We often see this among fans of musical groups,
cartoons, or film franchises.
The experiential mode may engage in similar signalling, but
usually to a lesser extent and for a different purpose:
to indicate skills and interests to 
potential collaborators,
rather than to assert an internal narrative to the outside world.

◊h3{Interface: how knowledge enables coordination}

We can see that the interior state is often influenced by 
the desire to communicate with others.
In the experiential mode, this communication is a means to
unlock new experiences, while in the narrative mode it can
allow for feelings of solidarity and kinship. We had previously
noted that the emergence of identity requires ◊i{relation}
alongside self-reference to form. The concept of ◊i{interface}
generalizes this by describing how identities allow people to
coordinate with one another---both between members of the community
and with outsiders who need their skills.

Interface is perhaps the most important part of identity in the
experiential mode. Because this mode sees identity as a means
for accessing new experiences, it is essential for identity to
imply a stable set of interactions with its members. For example,
an English speaker who seeks out fellow English speakers does so
not out of solidarity, but from a wish to communicate.  
A musician who seeks out fellow musicians does so for advice or
collaboration. A sick man who seeks out a doctor does so for
his expertise and judgment. The interior narratives are beside the
point in these cases---what matters is whether someone can perform
the needed task.

But interface is more than mere pragmatism. When we enter
a community with rich customs and kinship, we often find that
we can confide in fellow members on a deeper level than with
outsiders. In areas where our shared identity brings us together,
we can broach topics that most outsiders could not understand;
but even beyond these, a shared community can create a trust that
lets us share our deeper wants, fears, and dreams with others.
In that sense, identity allows for a deeper connection between
souls, enabling us to share our sentiments with a greater
precision and depth. It is here that the narrative mode
profits from interface, even if people's skills don't perfectly fit.

It is from this base that communities develop a rich set of
tools for their members to communicate. Cant, jargon, in-jokes,
symbols---these serve a dual role within groups.
They build trust by establishing a shared investment in a common
culture and cause; and they allow for more efficient communication
by compressing ideas that members are widely familiar with.
Both modes of identity profit from them, but
in different ways. The experiential mode benefits from their verbal economy,
while the narrative mode profits from the trust they cultivate among
members. But this dual role can risk vexing outsiders---think of
laymen who dismiss practical jargon as insider gatekeeping.

◊h3{Social capital: how knowledge is valued}

Concerns about gatekeeping are important because identities
are not value-neutral. Some can be very lucrative, while others
can be a burden. The nature of this value varies across identities.
Some are economically privileged, as we see with occupational licensing
in medicine. Others are legally privileged, as we see in societies
where certain groups are given quotas in hiring or education. And others
still are privileged on a social level, either because of stereotypes
that colour people's views of their members, or instead because of
claims to special knowledge through the ◊i{lived experience} of an identity.

The most basic form of social capital comes from the ability to perform.
For example, a musician may command respect because
of his ability to play music or analyze the structure of a song.
This kind of social capital does not inherently require identity,
as it is primarily structured around ability rather than community.
However, such social capital inevitably attaches to the labels themselves,
which serve as shorthand for ability or knowledge. 
For example, a
lawyer is expected to know law; a doctor is expected to know medicine;
and a pilot is expected to know how to fly an aircraft.

These forms of social capital are heuristic, with identity signalling knowledge
or practice. They can be legally protected via occupational licensing,
or used voluntarily. 
But the narrative mode also tends to create social capital that extends 
simply from
one's participation in a community and identity.
This is often called the ◊i{lived experience} of an identity's members. 
It emerges both from their inner lives and from how they are treated by outsiders.

Such knowledge is found both within individuals and
within the customs of the community (e.g. the jargon
and in-speak mentioned earlier).
It therefore comes from both their interior states and interface.
The idea is that membership in an identity can bear costs from
how outsiders treat the group. As a result, its members have a
lived experience that is unique and exclusive to them.
Governments may acknowledge this hidden value through legal measures
that seek to redress the
visible costs borne by its members---for example,
establishing systems such as affirmative action or quotas.

Social capital is ultimately downstream of identity, whether
as a simple heuristic of ability or a representation of experience.
But once an identity gains social capital, whether legal, economic,
or societal, it can then incentivize others to either join
the identity or seek similar benefits for their own group.

◊h2{Conflicts between identities}

It may be no exaggeration to state that many of the conflicts of the
past two hundred years have been at their core rooted in identity.
Conflicts of sectarian, ethnic, linguistic, or ideological identity have
animated wars spanning every continent and century since the Enlightenment.
Less fatally, conflicts over belonging in communities and the politics
between identity groups drive many of the great political controversies
of today.

The great breadth of identity-based conflict makes it impossible to
explain every one with a simple causal story. But there are several
recurring themes that we find among identity conflicts that can guide
us in understanding how they can crystallize so rapidly and to such
devastating effect.

Perhaps the most destructive type of identity conflict emerges
when members of identity groups feel that they are locked into
a ◊i{zero-sum} struggle with other identities---that is, a
struggle where one group's victory implies the other's loss.
These kinds of conflicts can emerge even in areas where
the stakes aren't inevitably zero-sum: for example, battles over
a shared culture where many peoples can exist side by side.

There are some domains in which conflicts are truly zero-sum.
The most basic is the use of force in society.
A group that gains a monopoly on violence can force all others
to do its bidding. Such a group can suppress or
destroy other groups by forcibly assimilating or expelling its
members. Furthermore, the mere possibility of this happening
can become self-fulfilling due to the ◊i{security dilemma},
as it can be hard to tell if another group is bearing arms
for self-defence or preparing to attack.

It is therefore very easy for fractured societies
to tip into war. In an anarchy or weak state,
the surest way to prevent the other tribe from attacking your own
is to gather arms and strike first. This is the process
that too often cascades into tragedy---one emerging not from moral
failure, but from an inability for groups to feel safe enough
to yield their arms to a sovereign.
When anarchy shocks a diverse, fluid society, its communities can
rapidly crystallize into rigid sectarian divides
that propagate throughout the country, as
identity suddenly becomes one's only guarantee of safety.
Think of the partition of India, where interfaith
communities were torn apart with devastating speed.

Even after a country has become stable, zero-sum conflict
between identity groups can migrate into the political system,
turning what should be the pursuit of inclusive growth into
spoils-seeking between different identities. We often find this
in countries that
have many small sectarian or linguistic factions while lacking
the broad-based economic growth that would normally discipline
such haggling.
Once certain identity groups are given social capital backed
by legal privilege,
other groups are tempted to lobby for privileges of their own,
leading to an escalatory spiral of factionalism.
We see this emerge in debates around affirmative action,
quotas, or even occupational licensing.

These are all true zero-sum conflicts.
But many conflicts emerge in places not traditionally seen
as zero-sum.
We see this especially in cultural matters.
Many identity groups, for example, try to cultivate
how their identity is perceived by broader society.
Attempts for outsiders to represent their experiences
are often met with criticism at the lack of ◊em{lived experience}
behind the work. While this can be useful to prevent
caricature, it also risks eroding the intergroup
empathy that allows authors 
and audiences to step into other people's worlds.

Similar debates emerge around ◊i{cultural appropriation}
with respect to cultural exports, with identity groups
often being protective of the ◊i{authenticity} of such products.
Consider a non-Mexican who sells tacos.
Such a restaurant may be criticized for lacking authenticity
by muting or sanitizing the cuisine for a domestic audience.
This isn't a true zero-sum conflict; indeed, mass appeal
often generates interest in more authentic versions.
But it is often seen as such. The result is that an identity group's image
becomes a kind of social capital that its members feel the need to 
protect---whether dress, food, language, or music.

Even if our Mexican restaurant is perfectly authentic in
technique, some may object to the perceived injustice in a socially
advantaged ethnic group profiting from the cultural
exports of a minority group. In this case, identity groups are
seeking to control who can extract rents from 
from their cultural practices, even if outsiders can do so
at a high quality. While concerns around technique and
authenticity are common in an experiential mode that
focuses on performance, concerns about who should profit
are more pronounced in a narrative mode
that emphasizes histories of injustice.

◊h2{Conflicts within identities}

While conflicts between identity groups can be explosive,
conflicts ◊em{within} identity groups are no less intense.
Unlike the former, they often result in drama and intrigue
rather than outright violence, as the stakes are seldom
as immediately life-or-death as fights over the use of force.
But given the tangible benefits of an identity to one's
conception of the self, one's ability to communicate with others
on a shared base, and one's ability to command social capital, 
the struggle over belonging and
membership within identity groups can become fierce.

◊h3{Trait-based identities}

One complication is that identities vary along an axis of
being ◊i{trait-based} and ◊i{performance-based}. The former
are tied to characteristics that inhere in certain people,
while the latter are focused more on shared abilities or knowledge.
For example, the African American identity is in great part
trait-based: a white man, however steeped in Black American culture,
music, and speech,
cannot really claim to be one if he is ethnically European.
In part, this is because skin colour itself leads to shared experiences
that stem from a person being ◊i{racialized}, something that a white person
cannot experience. In this sense, shared traits lead to shared experiences.

Yet once identities form around traits, they often accrue shared
customs and norms that can become so associated with the identity
that they become almost mandatory for members to be considered
◊i{authentic}. This is a key source of identity conflict.
Consider debates over whether a Black American who speaks only
Standard English is ◊i{acting white}. Or the extent to which an
African immigrant or his
children can be considered African American, even
though he shares the trait of skin colour and thus could be
racialized in the same way by others. The hinge is whether
skin colour is a mere hue, like hair colour, or a marker of
an identity-bearing culture---and if so, what that
culture entails.

That people care so much about policing membership may seem odd.
But there are several important reasons for it.
One is that an identity's claims to represent a ◊i{lived experience}
of its members weakens if the culture and norms of its members
become more diffuse. Because a group's social capital often
hinges on its claims to represent a certain lived experience,
this creates a collective action problem: members who
assimilate into broader society weaken the identity itself
by acting out of self interest.
This leads to charges of being ◊i{whitewashed}
or assimilated. Some argue that their dialect or
customs are holding them back; others accuse them of selling out
their cultural inheritance. 

This can make boundaries confusing for outsiders. For example,
is it racist to criticize the dialect of someone who speaks the
African-American Vernacular English (◊acro{AAVE})? There is nothing
genetically ◊i{black} about a dialect, and many black people
can and do speak Standard English equally well. Yet depending
on how intrinsic you feel ◊acro{AAVE} to be to 
Black identity, you might bristle at criticisms of
their dialect in a way you wouldn't for general Southern English,
despite the two dialects' shared roots.
We see such dynamics elsewhere; for example, among gay people
who draw deeply from gay culture in their choice of 
self-expression, versus those who see it as more incidental
and less essential.

Identity policing is not simply about conflicts
over cultural inheritance and its associated social capital,
as interface also plays an important role in both modes of identity.
In the experiential mode, assimilation can often lead to a death
spiral that makes cultural practices harder to maintain.
For example, speakers of a minority language that assimilate into
the dominant group cause the linguistic community to atrophy, which
both makes it harder and less useful for other members to
keep maintaining and passing on their tongue.
Those who speak the language may therefore shame those who
choose not to teach it to their children,
as we sometimes see within diaspora communities.

Interface matters in the narrative mode as well. 
As noted previously, a shared identity
provides a base upon which peers can share their sentiments
with a greater sense of depth and kinship. When an identity group
becomes more diffuse, this shared base erodes, making it harder
for people to connect on a level deeper than mere acquaintances.
Think of the class nerd who is mocked for speaking to his peers
as he would the teacher. His failure to code-switch
into their schoolyard cant marks him as an ◊i{other} whom they struggle to empathize with.
Even though he shares their trait of youth, 
this interface failure prevents him from fully belonging to the identity group.

◊h3{Performance-based identities}

While the previous identities often see conflict when
behaviours with contested value attach to personal traits,
many identities are rooted in behaviour itself---specifically,
the ◊i{performance} of certain tasks.
Defining identity by the ability to perform lets us
analyze groups that form around occupations and interests,
whether cooking, music, or video games.

Unlike with trait-based identities, members of performance-based 
groups do not feel
conscripted into debates about its boundaries and membership
simply by the accident of their birth. A black American may
tire of debates around ◊i{blackness}, or feel them irrelevant
to his life; but someone who enters into a community based around
performance is doing so of his own volition.

Yet there is still a great deal of conflict in a performance-based
identity. 
Take the term ◊i{engineer}. When analyzed in a purely experiential
mode, this term seems of little practical use. It includes
specialists in such a wide range of fields---aerospace, chemistry,
mechanics, electrical circuitry, material science---that a
manager who calls for an ◊i{engineer} must be more specific
if he wants his job done right. And yet people still fight
over who gets to call himself an engineer. Many countries legally
protect the role with occupational licensing. And those who call themselves
an engineer without holding a formal degree are often mocked.

For underspecified identities like that of the ◊i{engineer},
the narrative mode must play a significant part.
One possible reason is the desire to protect the occupation's social
capital. If an engineer is seen as a decorated expert, allowing any
hobbyist to use the term can be seen as debasement. Another may be
couched in personal resentment, as someone who worked hard for a degree
may wince at someone else claiming the title without having done the work.
When people
take on the role of ◊i{engineer}, they feel themselves to have entered into a
distinct culture and community---one that they want to cultivate and
distinguish from outsiders.

Of course, many conflicts over membership are more pragmatic in nature.
In the experiential mode, the most important use of an identity
is that it allows a person to gauge how others can help them
seek out a given service or experience. A sick man who needs a doctor
needs to know if the doctor can perform as one; a groom who wants
a musician to play at his wedding expects a certain level of competence.
Even social capital matters in the experiential mode, as occupational
licensing and other government policies mean that a doctor must
fit a certain set of criteria to be accepted for insurance and
government aid. This is social capital as legal privilege rather than
cultural clout.

◊h2{The tension between the experiential and narrative modes}

One of the greatest causes of conflict today is a basic
disagreement over what identity should be ◊em{for}.
We have already discussed the myriad uses
for identity: as a way of making the world's experiences more
legible, as a way of categorizing others, and as
a way of finding meaning. The exact purpose and utility of an
identity changes depending on whether one views their identities
narratively or experientially. Each mode of identity emphasizes
different things, and can thus be at odds. But because identity
is inherently relational---identified not just by self-reference
but also in relation to others---such disputes cannot be cured
with platitudes about letting live. They creep outward.

In the narrative mode, the ultimate source of identity is the
interior state; that is, how one conceives of themselves within
the world. Interface is important not only for bonding, but also
as a means of ◊i{validation}. In the narrative mode, to be
◊i{validated} is to know that you appear within other people's
interior worlds as you appear in your own---that how you feel
◊em{inside} is reflected in how they see you.

In the experiential mode, the first source of identity is
interface, anchored in the ability to perform. The interior
state is downstream of this, enriched by the range of experiences
enabled by one's abilities. Validation is knowing others can
rely on your or collaborate with you given your current capabilities.
Rejection can still hurt,
but it is not destabilizing in the same way.

Because members of an identity group may see their identity in
either the narrative or experiential mode, this can result in a tension.
Someone who sees himself as belonging to an identity, even if he does not
possess some characteristics common to the identity group, may
be dismissed by others simply because they feel the label is not useful for
describing how to interface with him. Yet that rejection, though done for
instrumental reasons, can wound his narrative sense of self.

This mismatch is where a lot of accusations of ◊i{gatekeeping} come from.
A gamer who only plays mobile games may not be considered as such by
other members of the identity group because there isn't a shared base upon
which to bond or confer. But that rejection, though done for instrumental
reasons, can wound one's sense of self. We also see this in many communities
relating to mental health and neurodivergence. 

Think of people who
call themselves ◊i{autistic} despite varying greatly in their ability to
function in society.
Some members of the community may accuse people
of using the term simply to excuse bad behaviour, as we've seen with
people like Kanye West or Elon Musk. Others instead may worry about the
identity becoming a mere shorthand for eccentricity, weakening its use
for people who genuinely need extra help to function in society. Yet these
concerns, while largely instrumental, are liable to be interpreted as a
personal attack, an ◊i{invalidation}, or a denial of the self. We also see this
with conditions like ◊i{obsessive-compulsive disorder}.

◊h2{Highly-developed narrative modes of identity}

The degree to which an identity is seen narratively is a spectrum.
Indeed, it can be taken very far. 
Many people seem to conceive of identity
as the result of an ongoing process of ◊i{questioning} in which they
successively discover more about their ◊i{true self}. 
We often see this intuitively broached with talk of ◊i{self-discovery}
or discovering ◊i{one's truth}.
Normally, the narrative mode allows for identity to bring meaning to
people's lives. 
But such people seem to have internalized identity to the point that
they see it as revealing a meaning that was inside them
all along, rather than bringing new meaning to life.

By analyzing the kind of language they use, we can reconstruct
a sophisticated, if implicit, metaphysics of the self. 
Identities become a shorthand for
modes of being that live within
the self. Discovering one's identity, and making it legible to the world,
is therefore not simply a way of finding one's place, but of expressing one's
◊i{true self}. Other people's acknowledgment of one's identity validates oneself
by affirming that this inner self has now become visible to the world.
This is sometimes referred to as living ◊i{authentically}.

Indifference, or even denial, by others is therefore not simply a rejection
of a label, but a denial of one's inner essence, and a challenge to its ability
to ◊i{exist} as instantiated in the physical world. When people whose identity is
invalidated accuse others of denying their ◊i{right to exist}, this is what they
mean---not necessarily that they are facing death threats, but that they are
denied the right to inhabit the world as they really are.

Not all people in the narrative mode go this far, and fewer still
explicitly conceive of identity in this way. This metaphysical model
is more a synchronic attempt to understand the internal logic of
identity as used by those with a highly developed narrative view.
Such a view elevates identity to a higher level of abstraction.
Earlier we examined three stages of identity formation in the
sentences 1) ◊i{I am cooking}, 2) ◊i{I cook}, and 3) ◊i{I am a cook}. But a highly developed
narrative mode yields a fourth sentence: 4) ◊i{I identify as a cook}.

Think about what that sentence implies. It does not say anything about what
one is doing, or what one ◊em{can} do. It is rather a pure assertion of interior
state along narrative lines: I acknowledge the existence of the category
of ◊i{cook}, and assert that there is an alignment between my interior state
and that category. Whereas the first two sentences were structured around
actions, and the third compressed actions, customs, and shibboleths into
a noun, this new sentence is not about cooking or being, but ◊i{identifying}.
It is about how one places the self relative to others. It also invites a
response from others: ◊i{this is how I
see myself. Is this how you see me?}

This is where identity conflict can emerge. Those who define identities
as being able to engage in certain behaviour might reject someone's membership
within the identity for purposes of interface; yet interface is itself
the means by which the developed narrative mode experiences validation.
You cannot simply solve the tension with appeals to ◊i{live and let live},
because interface is inherently relational, and so conflicts over how it
should be used produce an outward creep. 
That is a difficult challenge for liberal societies whose natural
response is pluralism.
Indeed, this is the underlying tension that animates much of
our culture-war debates about identity today.

◊h2{Idealized identity and the risk of reification}

People can clearly derive great meaning from identity.
But there is a risk of placing too much meaning in identities themselves,
especially in more abstract approaches that see identities as carrying
metaphysical content about oneself.
While it can often be too easy to imagine an archetypal ideal
of a member of a given identity group, 
identities are ultimately bundles of norms and practices that
are deeply socially contingent.
As a result, we can risk making sweeping judgments about people's
deeper selves based on shibboleths that are surprisingly recent
or inaccurate.

Consider the Black American identity. Aside from skin colour (which even then
shows great variation among its members), there is such a great diversity of
accents, interests, dress, and political views that it is hard to define an
ideal form of ◊i{Blackness} against which we judge individuals. 
This diversity is just as true today as it was among Northern and Southern
Black communities a century ago.
As discussed, people can fairly criticize others for not carrying
on their inherited customs.
But to argue that such a person is not truly Black or has ◊i{rejected}
Blackness on a metaphysical level would become more fraught, because it would assert
an ideal form of Blackness that does not truly exist.

We see similar things with gender. Masculinity itself is downstream of
cultural norms and behaviours that have varied enormously over time and place,
making it fraught to define an ideal ◊i{male identity} that doesn't exclude a
large swath of men. Yet despite this, too many young men are bullied, or
even have their maleness questioned, for not measuring up to this implicit 
◊i{ideal} form of masculinity. This ideal ◊i{maleness}
is assumed to exist not from anything intrinsically male, but from cultural
behaviours that happened to accrue to a visible subset of men in the current
era. 

One only has to look at European male fashion of the 18th century, with
its silk stockings, frilly clothes, powdered wigs, and clean-shaven faces,
to see how contingent these ideas are. Treating these attached behaviours
as determining, rather than deriving from, masculinity risks
subordinating men to
categories that have never perfectly fit any of them. 
It risks turning gender into a straitjacket, rather than
a canvas for one's own personal passions and dreams. 
The risks are not exclusive to race and gender;
idealizing identity can equally lead to an imposed uniformity along
nationalist, linguistic, or religious lines.

This is not to deny that identities have value. Because they compress
information into socially legible categories, they will naturally
blur around the edges. But that is all the more reason to avoid ◊i{reifying}
them, turning them into seemingly inevitable categories that
dictate how people live their lives regardless of the resulting tolls.
We must avoid letting identities become sovereign over the people
they are meant to describe.

◊h2{The lessons}

An identity is ultimately an abstraction of an abstraction.
It emerges from shared patterns of behaviour over groups of people
who become aware of the part they play in something bigger than themselves.
This is both a blessing and a curse. It means that identities will
always be fuzzy and contested. It means that people will always
wonder who truly belongs, and what an identity really means. But
because identities are downstream of human beings, they are
also liberating: identities are meant to serve us. We choose to use them
because they make our lives better.

The reason that identity has become such a fountain of conflict
is that it is used to compress 1) the world around us, 2) other people,
and 3) our own sense of self into a single word. It is a shorthand that allows
us to seek out new experiences by finding people who are able to bring them
to us. It allows us to quickly assess others as peers, mentors,
friends, and strangers. And it allows
us to understand our own place in the world as part of an intergenerational
narrative. 

Identities are often popular as a useful heuristic, even when inaccurate
or outright offensive. 
But they also emerge because they supply a meaning and sense
of belonging that people value. Tensions exist both between and within
these different uses of identity, and they cannot be confined to personal
pluralism because identity is naturally relational and thus outward-looking.

What can make identity conflict tricky is that it often emerges
in ways that don't immediately make themselves obvious.
A sports or anime fan might make the identity central to his life
and self-expression in ways that bemuse other people, even those who
share the hobby. Understanding how some people engage with identity
narratively versus experientially makes the reason obvious; but to
outsiders, it might instead seem like an unhealthy obsession.
Similarly, the pain of rejection might hurt someone far more than
others had expected because of the different understanding people have
of validation.

In many cases, focusing on identity itself can obscure matters.
The violence during the Partition of India was not simply a product
of ancient hatreds, but a rational response to state collapse under
uncertainty. Similarly, national identities can emerge almost at
a stroke in the fires of war or oppression. While the axis upon which
the identity coalesces can almost seem to be a historical accident---◊i{why
language? why religion?}---they are no less real once formed.

It can seem paradoxical that something so fuzzy can be so fateful.
Yet identities are important because they are useful---too useful, perhaps,
for their own good. That does not make identity bad, in any of its forms.
Both the narrative and experiential mode clearly have use.
But it is important to remember that identities are downstream of
who we are, and thus to avoid placing them above ourselves.
