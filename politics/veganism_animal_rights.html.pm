◊initial-cap{The moral worth} of animals is one of man's oldest ethical
quandaries. 
The debate has hardly mellowed with age, as
ancient debates over methods of husbandry and slaughter
have been augmented in recent years by more bloodless questions about
aggregate harms and animal rights. 
But this perennial puzzle makes a useful case study for the
tension at the heart of human society: the need to make compromises
under scarcity, uncertainty, and moral fog. In particular, more contemporary
approaches around animal rights and veganism cleanly illustrate the
ethical tradeoffs that result in the moral economy and political institutions
of a plural society.

◊h2{Veganism and moral economy}

Start with veganism and other duty-based approaches to animal ethics. 
Vegans give a variety of arguments for shunning meat and other direct forms 
of animal use. Some are grounded in the shared dignity of living creatures,
with an emphasis on the cruelty of slaughter. Others take a more utilitarian
and empirical approach, 
arguing that avoiding animal husbandry is a reliable way to minimize the 
unnecessary death of living beings. Given the burden of such a commitment,
it is hard to doubt their sincerity.

But trying to quantify the exact effects of such an approach reveals the
underlying tension. All human consumption sits at the intersection of cost
and benefit: the utility gained from consumption makes the initial costs
worthwhile, whether those benefits be through health, happiness, or
productivity. A market economy allows us to weigh these benefits against
the scarcity of labour and physical resources. Each unit of labour or physical
goods is given a unit cost in a shared currency. 

This not only lets us smoothly
taper our own consumption relative to our other wants---for example, buying
fewer bags of coffee to save money for rent. It also lets us make tradeoffs
between substitutes in the same category---for example, buying more instant
coffee to save money over whole beans. The result is that our toll on the
world's resources is held taut against the benefits they provide us; and any
dollar that can bring us more for less can spur us to act quickly. These
twin concepts of ◊em{marginal tradeoffs} and ◊em{fungibility} are what make the
system work.

This is where the key flaw of veganism emerges when assessed on utilitarian
grounds: it lacks a coherent moral economy. Popular veganism
assigns all direct consumption an infinite moral cost
(more precisely, it is ◊i{lexically prior}), while leaving indirect
consumption effectively unpriced. The problem is that this
moral economy fails on both counts. First, it doesn't allow us to make marginal
tradeoffs, because an infinite cost is inherently unbeatable.
Imagine the mayor wanted to pave a rural road. He estimated that the
project would kill a dozen deer. 
Would that be worth it, given its harm to animals?
What about a half-dozen deer? Without tradeoffs the question
is unanswerable.

The lack of fungibility also causes problems. What if instead the road killed 
a dozen squirrels instead of deer? Or a dozen ants, or spiders? Would that
make the project more defensible? 
Of course, most vegans would ignore the problem entirely, as it doesn't
involve the direct consumption of animals. 
Yet the fact that veganism
leaves such large swathes of human activity effectively unpriced means that it is
impossible to guarantee that a society following its tenets would actually
reduce animal harm. There are too many free variables.

It may seem unfair to press veganism in such an example, when the
framework focuses on other domains of human activity such as agriculture.
But even here, the lack of fungibility makes things hard: is killing a single
cow worse than killing a hundred beetles with pesticides? The strongest
argument that raising livestock causes more harm than plants is not due
to mammalian worth, but rather trophic efficiencies: raising cattle requires
growing far more plants to use as feed than if we simply ate the plants
directly. In that sense, it is very plausible that a herbivore society would
need to kill fewer animals to eat.

Unless we weight mammalian worth very highly, the order-of-magnitude increase
in harm to produce the feed likely dominates the total harm of livestock.
But note the implication. If husbandry's greatest sins are not of kind but
degree, such harms scale with ◊em{all} forms of consumption. 
In this sense, meat is not special:
all human consumption correlates with harm to animals, because land
is scarce and our lives overlap. 
Meat causes more harm because it consumes more resources. 
But if people saved money by eating plants exclusively,
they would likely shift their consumption to
travel, sport, or real estate, which also harm ecosystems and animals---a 
◊i{substitution}, not reduction.

Meanwhile, more sophisticated approaches that try to graft a moral economy onto veganism
risk becoming intractable.
Such a task would require valuing every animal life in some common unit of account,
and then determining the ◊i{social cost} of that basic unit in dollars.
This would not only be highly subjective;
it would be mathematically chaotic, with the results being highly sensitive
to the underlying assumptions.
Agreeing on any such framework would be impossible without some amount of 
hindsight fudging. 
Assessing a ◊i{social cost of carbon} in climate economics is already fraught,
but this would be far worse, 
sprawling out 
into an entire command economy of moral pricing. 

We can see that veganism fails as a heuristic due to its capricious
pricing of harm, while any finer-tuned system becomes untenable.
Neither solution appears to be a better heuristic of animal harm than 
a general measure of consumption. It is non-distortionary, broad-based,
and doesn't require any complex accounting. 
As a result, shifting government
revenue to a consumption tax may oddly be the soundest means of promoting
animal welfare practically possible.
Veganism is just not that. It is rather a duty-based approach that focuses more
on an intuitive moral hygiene than mathematical rigour.

◊h2{Animal rights and political economy}

Let us now examine another contemporary movement in animal ethics: the animal
rights movement. We have seen a push to bring suit against men and corporations
not only by their fellow men, but increasingly
on behalf of animals and nature itself. This is a novel approach to animal
welfare. Yet while it can
feel morally satisfying to have nature itself win a case against a scoundrel,
it brings institutional risks. Such a critique does not deny the moral worth
of animals. That has long been recognized in human custom. 
The question is whether
legal rights for individual animals are a workable approach. 

Human societies have long
had a rich set of customary rights and norms governing animal use, but these are
enforced ◊i{ad hoc} and through negotiation rather than judges, lawyers, and the
Leviathan of the state.
Legal rights in this context are pragmatic and instrumental—their job is to allow men
to live together without coming to blows by regulating the use of force. As Burke
wrote in a flash of realist insight, 
◊q{government is a contrivance of human wisdom to provide for human wants.} 
It is created by men not to bring heaven to Earth, but to allow them to live
together while negotiating their differences.


Whatever the moral or metaphysical justifications people give for individual 
human rights, the reality is much simpler. In self-government, society is most 
stable when each person within it feels safe. The most robust way of achieving 
this is through reciprocity: I agree to respect other people's dignity so that 
my own dignity will be respected. If I start to trample on other people's 
dignity, I am essentially making a bet that I am stronger than the collective 
ire of every other person in society—a risky gamble. 

The benefit of such a system is that it is predictable, since punishment emerges entirely from one's own actions; and it is based on action rather than factional loyalty. This makes it an easy sell to all members of a society, no matter how physically weak or poor, and lets them pool their power behind a rule-bound state that towers over even the greatest rogues and scoundrels.

The system works because it is anchored in the ◊i{interchangeability} of all humans in society. This is what allows it to protect even the weakest among us, and its limits show clearly in our institutional fabric. The disabled, who cannot fight for themselves, are protected because each of us has the capacity to become disabled. I respect the disabled so that I should be so respected if something happens to me. I respect the sick, and the dying, and the unconscious, for the same reason. 

The exceptions prove the rule: having become an adult, I cannot become a child once more. And indeed, children tend to have fewer personal rights in our societies, with parents being given more discretionary power over them. But even so, we still guarantee children a baseline of rights as human beings. For weakening the dignity of human life, however young or old, has a chilling effect on all of us. That human dignity is interchangeable yet supreme is what holds the system together.

We can see that individual human rights are not a moral nicety imported into our system of government. They ◊em{are} the system. Whenever we add extra conditions to our legal foundations, such as giving a legal personhood to animals or nature, we burden these institutions with extra constraints not necessary to their stability. Indeed, if treating animals this way puts strain on human welfare (for example, by making food more expensive or threatening human culture), it opens up a space for populists to claim that the voters are being ill-served by their laws and government.

The argument is not simply that change is divisive, and so should always be avoided.
Indeed, many of the greatest social reforms were a vindication of human dignity
through reciprocity and interchangeability. Take the abolition of slavery, for 
example. Abolitionists did not think that the institution put them
directly at risk of enslavement. But many argued that a society in 
which a segment of human beings could be dominated by Slave Power made
despotism more likely for all. Marx explicitly opposed slavery because he saw it
as portending a future domination of ◊em{all} workers by wealthy capitalists.
For critics, excuses of race were a diversion from the deeper, more universal issues.

Even aside from this, a framework of personhood is ill-fitting for animals, because
there are too many cases where we need to treat animals collectively rather than
individually. Think of feral animals that endanger birds, or invasive species that
threaten ecosystems. We may need to cull animals to preserve the whole, which is 
incompatible with an individual view of animal rights. This need to sometimes
view animals as collectively valuable clashes with personhood in a way that
no other expansion of the category ever did. It raises serious questions about whether
such an expansion is the right approach.

This doesn't mean that laws protecting animals cannot exist, although calling them ◊i{animal rights} distorts their intrinsically human nature. The most defensible laws are framed as constraints on human behaviour that seek to minimize gratuitous violence while avoiding burdensome costs, rather than creating a legal fiction of animals as plaintiffs. Such fictions too often lapse into moral ventriloquism, giving power not to animals but to those who affect to speak for them. The resulting fog risks breeding cynicism—the idea that the legal system is a web of fictions meant to privilege the strong over the weak.

The recent trend of populists trying to ban lab-grown meat clearly illustrates the case.
Such bans don't immediately make sense on their face, as giving customers the
option to buy such products doesn't take away their right to buy meat from livestock.
While some may see it as just a sop to farmers, many politicians 
◊a[#:href "https://www.bbc.com/news/world-us-canada-68947766"]{explicitly frame it} 
as a reaction to animal-rights absolutism: ◊i{if lab-grown meat takes off,
the elites are going to foist it on working families. Better to nip it in the bud 
beforehand.} Culinary terms have now been enlisted in the culture war—is there
a more immediately evocative image than the ◊i{soy latte}? The
populist rage against a caricatured elite is seductive, and growing.

Tragically, the problem with animal rights advocates is not the moral substance of their claims. It is rather the political economy of their prescriptions. I believe that they are animated by an admirable zeal, but their desire to assert the unity between man and his fellow creatures should be rhetorical rather than legal. Government is not meant to give flesh to metaphysical truth, but to hold a plural society together. We all love animals. That alone should give their advocates hope.

◊h2{Conclusion}

These case studies show the risks of using duty-based or metaphysical
frameworks as an approach to public ethics.
Human societies have developed a diverse set of moral rules and customs
that help us cope with the tragic nature of life and death that animates
this world. Veganism and animal rights follow in this approach; they do
not sit apart from them, nor are they intrinsically better than their
venerable peers. The best approach then is to avoid absolutist, moralizing claims about
animal rights. It is one of man's oldest debates; that it has festered
unresolved for so long shows that there is no obvious answer to it.

Grounded in pluralism and mutual respect, we can then try to find
points of alignment between human dignity and animal dignity.
We have long forbidden sadism, torture, and abuse of animals for this reason.
Further improvements should be in this mould. As our knowledge grows, it should
raise our ambitions, allowing us to reduce suffering without forgetting human needs.

Most of these values can and 
should remain customary and negotiated. But if government is made in the
service of human wants, we should not forget that most humans want animals
to live happy lives. 
The constraint is not our indifference to suffering, but the world's. 
And as we navigate this vast cycle of life and death, we must strain to avoid
sacrificing our own needs to a fruitless idealism—or giving in to an ashen
cynicism.
